function normalizeLocale(code: string | undefined): SupportedLocale {
  if (!code) return 'en';
  const base = code.split('-')[0]?.toLowerCase();
  return (SUPPORTED_LOCALES as readonly string[]).includes(base) ? (base as SupportedLocale) : 'en';
}

